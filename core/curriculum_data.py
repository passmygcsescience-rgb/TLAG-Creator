# Export all curricula
AQA_GCSE_CURRICULA = {
    "Chemistry": CHEMISTRY_8462,
    "Biology": BIOLOGY_8461,
    "Physics": PHYSICS_8463
}

def get_topics_list(subject: str) -> list:
    """Get flat list of all topics for a subject."""
    curriculum = AQA_GCSE_CURRICULA.get(subject)
    if not curriculum:
        return []
    
    topics = []
    for topic_id, topic_data in curriculum["topics"].items():
        topics.append({
            "id": topic_id,
            "name": topic_data["name"],
            "subtopics": list(topic_data.get("subtopics", {}).keys())
        })
    return topics

def get_subtopic_content(subject: str, topic_id: str, subtopic_id: str) -> list:
    """Get content for a specific subtopic."""
    curriculum = AQA_GCSE_CURRICULA.get(subject)
    if not curriculum:
        return []
    
    topic = curriculum["topics"].get(topic_id)
    if not topic:
        return []
    
    subtopic = topic.get("subtopics", {}).get(subtopic_id)
    if not subtopic:
        return []
    
    return subtopic.get("content", [])
